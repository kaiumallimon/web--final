<?php 
# pi
echo "Pi: " .pi() ."\n";

# min-max
echo("Minimum:" .min(0,150,3,-5) ."\n");
echo("Maximum:" .max(0,150,3,-5) ."\n");

# absolute (positive)
echo "Abs: " .abs(-6.7) ."\n";

# sqrt:
echo "Sqrt:" .sqrt(64) ."\n";

# round
echo "Round:" .round(0.59) ."\n";

# random number:
echo "Random number:" . rand() ."\n";


# random number from - to -:
echo "Random number:" . rand(100000,999999) ."\n";
?>