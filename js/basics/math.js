// JavaScript Math: Math object, common methods, and operations.

// Pi
console.log("Pi:", Math.PI);

// Min-Max
console.log("Minimum:", Math.min(0, 150, 3, -5));
console.log("Maximum:", Math.max(0, 150, 3, -5));

// Absolute (positive)
console.log("Abs:", Math.abs(-6.7));

// Sqrt
console.log("Sqrt:", Math.sqrt(64));

// Round
console.log("Round:", Math.round(0.59));

// Random number (0 to 1)
console.log("Random number:", Math.random());

// Random number from 100000 to 999999
console.log("Random number:", Math.floor(Math.random() * (999999 - 100000 + 1)) + 100000); 